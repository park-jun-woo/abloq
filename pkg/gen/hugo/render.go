//ff:why [permalinks] 블록을 내지 않는다 — Hugo 기본 경로 URL(파일명=slug, 번들=디렉토리명)이 게이트의 slug 계약과 정확히 일치하고, :slug 토큰은 제목 폴백이라 비ASCII 제목에서 계약을 깬다 (Phase005에서 발견)
package hugo

import (
	"fmt"
	"strconv"
	"strings"

	"github.com/park-jun-woo/abloq/pkg/blogyaml"
)

// Render produces hugo.toml deterministically from blog.yaml.
// The first entry of languages becomes defaultContentLanguage.
func Render(b *blogyaml.Blog) []byte {
	defaultLang := ""
	if len(b.Languages) > 0 {
		defaultLang = b.Languages[0]
	}
	var sb strings.Builder
	sb.WriteString("# generated by abloq from blog.yaml — do not edit\n")
	fmt.Fprintf(&sb, "baseURL = %s\n", strconv.Quote(b.Site.BaseURL))
	fmt.Fprintf(&sb, "title = %s\n", strconv.Quote(b.Site.Title))
	fmt.Fprintf(&sb, "defaultContentLanguage = %s\n", strconv.Quote(defaultLang))
	sb.WriteString("defaultContentLanguageInSubdir = true\n")
	sb.WriteString("enableRobotsTXT = false\n")
	sb.WriteString("\n[params]\n")
	fmt.Fprintf(&sb, "author = %s\n", strconv.Quote(b.Site.Author))
	sb.WriteString("\n[sitemap]\n")
	sb.WriteString("changefreq = \"weekly\"\n")
	sb.WriteString("filename = \"sitemap.xml\"\n")
	sb.WriteString("priority = 0.5\n")
	sb.WriteString(renderLanguages(b.Languages))
	return []byte(sb.String())
}
