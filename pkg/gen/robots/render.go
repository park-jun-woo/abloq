package robots

import (
	"fmt"
	"strings"

	"github.com/park-jun-woo/abloq/pkg/blogyaml"
	"github.com/park-jun-woo/abloq/pkg/bots"
)

// Render produces static/robots.txt: one User-agent group per dictionary bot
// (allow = empty Disallow, block = "Disallow: /"), a default *-group and the sitemap URL.
func Render(b *blogyaml.Blog) []byte {
	var sb strings.Builder
	sb.WriteString("# generated by abloq from blog.yaml geo.crawlers — do not edit\n")
	category := ""
	for _, bot := range bots.All() {
		if bot.Category != category {
			category = bot.Category
			fmt.Fprintf(&sb, "\n# %s\n", category)
		} else {
			sb.WriteString("\n")
		}
		rule := "Disallow:"
		if resolvePolicy(b.Geo.Crawlers, bot) == "block" {
			rule = "Disallow: /"
		}
		fmt.Fprintf(&sb, "User-agent: %s\n%s\n", bot.UserAgent, rule)
	}
	sb.WriteString("\nUser-agent: *\nAllow: /\n")
	fmt.Fprintf(&sb, "\nSitemap: %s/sitemap.xml\n", strings.TrimRight(b.Site.BaseURL, "/"))
	return []byte(sb.String())
}
